# сумму чисел a и b;
a, b = int(input()), int(input())
print(a + b)
# разность чисел a и b;
print(a - b)
# произведение чисел a и b;
print(a * b)
# частное чисел a и b;
print(a / b)
# целую часть от деления числа a и b;
print(a // b)
# остаток от деления числа a и b;
print(a % b)
# корень квадратный a и b из суммы их 10-х степеней:
print((a ** 10 + b ** 10) ** 0.5)
